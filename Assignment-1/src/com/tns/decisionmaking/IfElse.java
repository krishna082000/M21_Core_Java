package com.tns.decisionmaking;

import java.util.Scanner;

public class IfElse {

	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		int i=s.nextInt();
		if(i>=20)
		{
			System.out.println(i);
		}
		else
		{
			System.out.println("Input value is less than 20");
		}
			

	}

}
